package com.jiaye.cashloan.view.data.loan.auth.source.file;

/**
 * LoanAuthFileModel
 *
 * @author 贾博瑄
 */
public class LoanAuthFileModel {

    /*名称*/
    private String name;

    /*类型*/
    private int type;

    /*图标*/
    private int icon;

    /*数量*/
    private int count;

    /*图标背景*/
    private int icBackground;

    /*文字颜色*/
    private int color;

    /*状态图标*/
    private int icState;

    /*背景*/
    private int background;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getIcBackground() {
        return icBackground;
    }

    public void setIcBackground(int icBackground) {
        this.icBackground = icBackground;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getIcState() {
        return icState;
    }

    public void setIcState(int icState) {
        this.icState = icState;
    }

    public int getBackground() {
        return background;
    }

    public void setBackground(int background) {
        this.background = background;
    }
}
